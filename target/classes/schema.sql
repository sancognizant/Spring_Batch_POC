CREATE TABLE  person (
  id INTEGER  NOT NULL AUTO_INCREMENT,
  firstName VARCHAR(64),
  lastName VARCHAR(64),
  birthDate VARCHAR(64),
  bankruptcyDate VARCHAR(64) ,
  bankruptcyReason VARCHAR(64) ,
  bankruptcyChapter VARCHAR(64) ,
  startDate VARCHAR(64) ,
  SSNCode VARCHAR(64) ,
  zipCode VARCHAR(64) ,

  server VARCHAR(64) ,
  ruleset VARCHAR(64) ,
  _user VARCHAR(64),
  password VARCHAR(64) ,

  yearlyIncome float ,
  creditScore float ,
  amount float ,
  duration float ,
  yearlyInterestRate float ,
  showtrace INT ,
  PRIMARY KEY (id)
);

